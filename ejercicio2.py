# Escribir un programa que muestre por pantalla
# el resultado de la siguiente operación aritmética
# (3+2 / 2.5) ^ 2

print("Ejercicio 2: (3+2 / 2.5) ^ 2")
G1 = 3 + 2  # Primer miembro de la división
G2 = 2 * 5  # Segundo miembro de la división
G3 = G1 / G2  # División
resultado = int(G3) ** 2  # Resultado final elevándolo al cuadrado
print("El resultado es:", resultado)
quit(print("Fin del código"))
